// Helper functions
export const getProvinces = () => {
  return Object.keys(indonesiaLocations).sort();
};

export const getCitiesAndRegencies = (province) => {
  if (!indonesiaLocations[province]) return { cities: [], regencies: [] };
  
  const cities = Object.keys(indonesiaLocations[province].cities).sort();
  const regencies = indonesiaLocations[province].regencies.sort();
  
  return { cities, regencies };
};

export const getRegenciesForCity = (province, city) => {
  if (!indonesiaLocations[province] || !indonesiaLocations[province].cities[city]) {
    return [];
  }
  return indonesiaLocations[province].cities[city].sort();
};

export const getAllCitiesAndRegencies = (province) => {
  if (!indonesiaLocations[province]) return [];
  const cities = Object.keys(indonesiaLocations[province].cities);
  const regencies = indonesiaLocations[province].regencies;
  return [...cities, ...regencies].sort();
};
